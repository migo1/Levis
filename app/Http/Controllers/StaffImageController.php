    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if($request->hasFile('photo')){
            // Get filename with the extension
            $filenameWithExt = $request->file('photo')->getClientOriginalName();
            // Get just filename
            $filename = pathinfo($filenameWithExt, PATHINFO_FILENAME);
            // Get just ext
            $extension = $request->file('photo')->getClientOriginalExtension();
            // Filename to store
            $fileNameToStore= $filename.'_'.time().'.'.$extension;
            // Upload Image
            $path = $request->file('photo')->storeAs('public/cover_images', $fileNameToStore);
        } else {
            $fileNameToStore = 'noimage.jpg';
        }
        // Create Post
        $staffimage = new StaffImage;
        $staffimage->user_id = $request->input('user_id');
        $staffimage->photo = $fileNameToStore; 

        $user_id = $staffimage->user_id;


        $found = StaffImage::where('user_id', $user_id)->count();

           if($found == 0) {
        $staffimage->user_id = $user_id;
        $staffimage->save();
        return redirect()->back();
           }
        
    }

